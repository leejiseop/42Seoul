#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	length_str;
	char	*str;
	int		i;

	length_str = ft_strlen(s1) + ft_strlen(s2);
	if (!(str = (char *)malloc(sizeof(char) * (length_str + 1))))
		return (NULL);
	i = 0;
	while (*s1)
		str[i++] = *s1++;
	while (*s2)
		str[i++] = *s2++;
	str[i] = '\0';
	return (str);
}
